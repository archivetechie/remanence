/// One full parity epoch that resume must rebuild and emit as an ordinary
/// sidecar tape file before accepting new object data.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeSidecarPlan {
    /// Epoch ID implied by the protected ordinal range.
    pub epoch_id: u64,
    /// First protected object-data ordinal.
    pub protected_ordinal_start: u64,
    /// End-exclusive protected object-data ordinal.
    pub protected_ordinal_end_exclusive: u64,
}

/// Pure plan for reopening a tape from the committed filemark-map prefix.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeAppendPlan {
    /// Last catalog-committed tape file in the prefix. Physical append starts
    /// just after this file's trailing filemark.
    pub append_after_tape_file_number: u32,
    /// Physical block-position hint immediately after the committed prefix.
    pub append_position: PhysicalPositionHint,
    /// Protection watermark loaded from the committed prefix before any
    /// open-epoch rebuild sidecars are emitted.
    pub highest_protected_ordinal_before_rebuild: u64,
    /// Protection watermark expected after all planned rebuild sidecars are
    /// emitted and committed.
    pub highest_protected_ordinal_after_rebuild: u64,
    /// Start ordinal of the live epoch that remains loaded after rebuild.
    pub live_epoch_start: u64,
    /// Next object-data ordinal for newly appended object blocks.
    pub next_data_ordinal: u64,
    /// Bare monotonic id of the live epoch at `live_epoch_start`.
    pub next_epoch_id: u64,
    /// Full epochs in `[W, T)` that an explicitly scoped legacy/forensic
    /// rebuild must emit as ordinary sidecars. The production v1 append path
    /// rejects committed prefixes whose W/T gap reaches one full epoch.
    pub sidecars_to_emit: Vec<ResumeSidecarPlan>,
}

/// Result returned after resume has positioned to the committed prefix and
/// committed any sidecars generated by open-epoch rebuild.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeAppendResult {
    /// Last catalog-committed tape file that established the original append
    /// point for this resume operation.
    pub append_after_tape_file_number: u32,
    /// Sidecars emitted during resume and committed through the ordinary
    /// sidecar transaction path.
    pub sidecars_emitted: Vec<SidecarTapeFile>,
    /// Protection watermark after resume-generated sidecars have committed.
    pub highest_protected_ordinal: u64,
    /// Start ordinal of the live epoch that remains loaded after rebuild.
    pub live_epoch_start: u64,
    /// Next object-data ordinal for newly appended object blocks.
    pub next_data_ordinal: u64,
    /// Bare monotonic id of the live epoch at `live_epoch_start`.
    pub next_epoch_id: u64,
}

/// Encoded sidecar reconstructed by rereading the committed open epoch.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeRebuiltSidecar {
    /// Planned epoch/range this encoded sidecar satisfies.
    pub plan: ResumeSidecarPlan,
    /// Complete sidecar tape-file bytes to write before committing the
    /// ordinary sidecar catalog transaction.
    pub encoded: EncodedSidecarTapeFile,
}

/// Rebuilt state for the final partial epoch that remains live after resume.
///
/// The current rebuild bridge stores the partial data shards across the
/// resume handoff so the writer can validate CRCs and reconstruct the live
/// parity accumulators before accepting new object data. The public shape
/// makes the resume boundary explicit; a later bridge can carry parity
/// accumulators directly without changing the catalog-driven append-point
/// rule.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeLiveEpochState {
    /// Bare monotonic epoch identifier.
    pub epoch_id: u64,
    /// First ordinal in the live epoch.
    pub protected_ordinal_start: u64,
    /// Next object-data ordinal after the rebuilt partial epoch.
    pub next_data_ordinal: u64,
    /// Number of real data shards currently loaded in this epoch.
    pub data_blocks_in_epoch: u64,
    /// Per-stripe data buffers in row-major epoch order.
    pub stripe_buffers: Vec<Vec<Vec<u8>>>,
    /// CRC-64/XZ values for the real data shards in ordinal order.
    pub data_shard_crc64s: Vec<u64>,
}

/// Result of rereading `[W,T)` from the committed prefix.
#[derive(Clone, Debug, PartialEq, Eq)]
pub struct ResumeOpenEpochRebuild {
    /// Catalog-prefix append plan that drove this rebuild.
    pub plan: ResumeAppendPlan,
    /// Complete epochs rebuilt from committed object blocks and ready to emit
    /// as ordinary sidecar tape files.
    pub rebuilt_sidecars: Vec<ResumeRebuiltSidecar>,
    /// Final partial epoch to carry into the resumed writer, if any.
    pub live_epoch: Option<ResumeLiveEpochState>,
}

impl ResumeAppendPlan {
    /// Complete the pure plan after the caller has emitted and committed the
    /// planned resume sidecars.
    pub fn complete(
        self,
        sidecars_emitted: Vec<SidecarTapeFile>,
    ) -> Result<ResumeAppendResult, ParityError> {
        if sidecars_emitted.len() != self.sidecars_to_emit.len() {
            return Err(resume_error(format!(
                "resume emitted {} sidecars, expected {}",
                sidecars_emitted.len(),
                self.sidecars_to_emit.len()
            )));
        }

        for (index, (actual, expected)) in sidecars_emitted
            .iter()
            .zip(self.sidecars_to_emit.iter())
            .enumerate()
        {
            let expected_tape_file_number = self
                .append_after_tape_file_number
                .checked_add(1)
                .and_then(|value| value.checked_add(u32::try_from(index).ok()?))
                .ok_or_else(|| resume_error("resume sidecar tape-file number overflows"))?;
            if actual.tape_file_number != expected_tape_file_number
                || actual.epoch_id != expected.epoch_id
                || actual.protected_ordinal_start != expected.protected_ordinal_start
                || actual.protected_ordinal_end_exclusive
                    != expected.protected_ordinal_end_exclusive
            {
                return Err(resume_error(format!(
                    "resume sidecar {index} does not match planned epoch/range"
                )));
            }
        }

        Ok(ResumeAppendResult {
            append_after_tape_file_number: self.append_after_tape_file_number,
            sidecars_emitted,
            highest_protected_ordinal: self.highest_protected_ordinal_after_rebuild,
            live_epoch_start: self.live_epoch_start,
            next_data_ordinal: self.next_data_ordinal,
            next_epoch_id: self.next_epoch_id,
        })
    }
}

/// Replay the local journal into a validated committed-prefix map.
///
/// This is the v0.7.2 fast-resume entry point before raw-tape positioning and
/// open-epoch rebuild. It drops torn trailing journal records through
/// [`TapeFileJournal::load_committed`], validates the one-open-epoch restart
/// bound, and converts the committed rows into a structural filemark map.
pub fn committed_prefix_from_journal(
    journal: &dyn TapeFileJournal,
    scheme: &ParityScheme,
) -> Result<(CommittedState, FilemarkMap), ParityError> {
    let state = journal.load_committed()?;
    state.validate_v1_restart_bound(scheme)?;
    let map = state.filemark_map()?;
    Ok((state, map))
}

/// Rebuild bootstrap sidecar-directory rows from committed journal geometry.
///
/// Sidecars store `2H + P + 1` blocks, where `P = S*m`; therefore the
/// replicated header/index size `H` is recoverable without rereading tape.
pub fn sidecar_directory_from_committed_state(
    committed: &CommittedState,
    scheme: &ParityScheme,
) -> Result<Vec<SidecarEpochDirectoryEntry>, ParityError> {
    let parity_blocks = u64::from(scheme.stripes_per_neighborhood)
        .checked_mul(u64::from(scheme.parity_blocks_per_stripe))
        .ok_or(ParityError::Invariant(
            "sidecar parity block count overflows",
        ))?;
    committed
        .entries
        .iter()
        .filter(|entry| entry.kind == crate::filemark_map::TapeFileKind::ParitySidecar)
        .map(|entry| {
            let replicated_index_blocks = entry
                .block_count
                .checked_sub(parity_blocks)
                .and_then(|value| value.checked_sub(1))
                .ok_or(ParityError::Invariant("sidecar block geometry underflows"))?;
            if replicated_index_blocks == 0 || replicated_index_blocks % 2 != 0 {
                return Err(ParityError::Invariant(
                    "sidecar block geometry cannot recover replicated index size",
                ));
            }
            Ok(SidecarEpochDirectoryEntry {
                tape_file_number: entry.tape_file_number,
                epoch_id: entry
                    .epoch_id
                    .ok_or(ParityError::Invariant("journal sidecar missing epoch id"))?,
                protected_ordinal_start: entry.protected_ordinal_start.ok_or(
                    ParityError::Invariant("journal sidecar missing protected range start"),
                )?,
                protected_ordinal_end_exclusive: entry.protected_ordinal_end_exclusive.ok_or(
                    ParityError::Invariant("journal sidecar missing protected range end"),
                )?,
                sidecar_total_block_count: entry.block_count,
                sidecar_header_block_count: u32::try_from(replicated_index_blocks / 2).map_err(
                    |_| ParityError::Invariant("sidecar header block count overflows u32"),
                )?,
                parity_shard_block_count: u32::try_from(parity_blocks).map_err(|_| {
                    ParityError::Invariant("sidecar parity block count overflows u32")
                })?,
                canonical_metadata_hash: entry.canonical_metadata_hash.ok_or(
                    ParityError::Invariant("journal sidecar missing canonical metadata hash"),
                )?,
                flags: SIDECAR_DIRECTORY_FLAG_PRIMARY_KNOWN_GOOD
                    | SIDECAR_DIRECTORY_FLAG_TAIL_KNOWN_GOOD,
            })
        })
        .collect()
}

/// Plan resume append directly from a [`TapeFileJournal`].
pub fn plan_resume_append_from_journal(
    journal: &dyn TapeFileJournal,
    scheme: &ParityScheme,
) -> Result<ResumeAppendPlan, ParityError> {
    let (_state, committed_prefix) = committed_prefix_from_journal(journal, scheme)?;
    plan_resume_append_from_committed_prefix(&committed_prefix, scheme)
}

/// Reread committed object blocks above the protection watermark and rebuild
/// the open partial-epoch state required before a resumed append may accept
/// new data.
///
/// This implements the Layer 3c v0.4.4 §7.8 step-8 read/reaccumulate bridge:
/// `W = highest_protected_ordinal`, `T = total committed object ordinals`,
/// assert `T - W < data_ordinals_per_epoch`, reread `[W,T)`, and return the
/// partial epoch as live state. Full-epoch or multi-epoch rebuild is not a v1
/// production append path; tests keep it behind an explicitly named
/// legacy/forensic helper so the sidecar-emission bridge remains covered.
pub fn rebuild_open_epoch_from_committed_prefix(
    source: &mut dyn RawTapeSource,
    committed_prefix: &FilemarkMap,
    scheme: &ParityScheme,
    tape_uuid: [u8; 16],
    block_size: u32,
) -> Result<ResumeOpenEpochRebuild, ParityError> {
    let plan = plan_resume_append_from_committed_prefix(committed_prefix, scheme)?;
    rebuild_open_epoch_from_plan(
        source,
        committed_prefix,
        scheme,
        tape_uuid,
        block_size,
        plan,
    )
}

fn rebuild_open_epoch_from_plan(
    source: &mut dyn RawTapeSource,
    committed_prefix: &FilemarkMap,
    scheme: &ParityScheme,
    tape_uuid: [u8; 16],
    block_size: u32,
    plan: ResumeAppendPlan,
) -> Result<ResumeOpenEpochRebuild, ParityError> {
    if block_size == 0 {
        return Err(resume_error("resume rebuild block size is zero"));
    }
    source
        .configure_fixed_block_size(block_size)
        .map_err(|err| {
            resume_error(format!(
                "resume rebuild could not configure fixed block size {block_size}: {err}"
            ))
        })?;

    if plan.highest_protected_ordinal_before_rebuild == plan.next_data_ordinal {
        debug_assert!(
            plan.sidecars_to_emit.is_empty(),
            "W == T resume plan must not schedule rebuilt sidecars"
        );
        locate_resume_append_position(source, plan.append_position)?;
        return Ok(ResumeOpenEpochRebuild {
            plan,
            rebuilt_sidecars: Vec::new(),
            live_epoch: None,
        });
    }

    let rebuilt_epoch_count = u64::try_from(plan.sidecars_to_emit.len())
        .map_err(|_| resume_error("resume sidecar count does not fit u64"))?;
    let first_rebuild_epoch_id = plan
        .next_epoch_id
        .checked_sub(rebuilt_epoch_count)
        .ok_or_else(|| resume_error("resume rebuild epoch id underflows"))?;
    let mut accumulator = ResumeEpochAccumulator::new(
        scheme,
        tape_uuid,
        block_size,
        plan.highest_protected_ordinal_before_rebuild,
        first_rebuild_epoch_id,
    )?;
    for ordinal in plan.highest_protected_ordinal_before_rebuild..plan.next_data_ordinal {
        let block = read_committed_object_block(source, committed_prefix, ordinal, block_size)?;
        accumulator.push_block(ordinal, block)?;
    }
    let (rebuilt_sidecars, live_epoch) = accumulator.finish()?;

    validate_rebuilt_sidecars(&rebuilt_sidecars, &plan.sidecars_to_emit)?;

    locate_resume_append_position(source, plan.append_position)?;

    Ok(ResumeOpenEpochRebuild {
        plan,
        rebuilt_sidecars,
        live_epoch,
    })
}

/// Write resume-rebuilt sidecars through the raw tape sink, commit each one
/// after its synchronous filemark barrier, and finish the append plan.
///
/// This is the Layer 3c half of `docs/layer3c-design.md` §7.8 steps 8-9:
/// rebuilt sidecars are ordinary sidecar tape files. The `commit_sidecar`
/// callback is supplied by Layer 5 and must run the same catalog transaction
/// path used for sidecars emitted during normal object close. It is invoked
/// only after all sidecar blocks have written, the synchronous filemark has returned,
/// and a post-barrier `position()` capture has succeeded.
/// `expected_tape_uuid` is used to validate the encoded sidecar bytes before
/// any write, so a resume helper cannot commit another tape's sidecar.
pub fn emit_resume_rebuilt_sidecars_to_raw<F>(
    sink: &mut dyn RawTapeSink,
    journal: &mut dyn TapeFileJournal,
    plan: ResumeAppendPlan,
    rebuilt_sidecars: &[ResumeRebuiltSidecar],
    expected_tape_uuid: [u8; 16],
    commit_sidecar: F,
) -> Result<ResumeAppendResult, ParityError>
where
    F: FnMut(&SidecarTapeFile) -> Result<(), ParityError>,
{
    emit_resume_rebuilt_sidecars_to_raw_inner(
        sink,
        Some(journal),
        plan,
        rebuilt_sidecars,
        expected_tape_uuid,
        commit_sidecar,
    )
}

