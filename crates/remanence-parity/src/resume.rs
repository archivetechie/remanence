//! Restart/append planning for Layer 3c v0.4.4.
//!
//! The resume contract is catalog-prefix driven: a later write session appends
//! after the last catalog-committed tape file, not after the last object and
//! not after the parity-protection watermark. This module contains the pure
//! planning surface plus the raw-tape open-epoch reread and rebuilt-sidecar
//! emission bridge.

