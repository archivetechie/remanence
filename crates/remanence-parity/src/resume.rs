fn validate_tail_coherence(
    tail: &TapeFileMapEntry,
    total_ordinals: u64,
    live_epoch_start: u64,
    epoch_data_shards: u64,
) -> Result<(), ParityError> {
    if tail.kind == TapeFileKind::Object {
        let first = tail.first_parity_data_ordinal.ok_or_else(|| {
            resume_error(format!(
                "tail object tape file {} is missing first data ordinal",
                tail.tape_file_number
            ))
        })?;
        let end = first.checked_add(tail.block_count).ok_or_else(|| {
            resume_error(format!(
                "tail object tape file {} ordinal range overflows",
                tail.tape_file_number
            ))
        })?;
        if end != total_ordinals {
            return Err(resume_error(format!(
                "tail object tape file {} ends at ordinal {end}, expected total committed data ordinals {total_ordinals}",
                tail.tape_file_number
            )));
        }
    }

    let live_epoch_len = total_ordinals
        .checked_sub(live_epoch_start)
        .ok_or_else(|| resume_error("live epoch start exceeds committed data ordinals"))?;
    if live_epoch_len >= epoch_data_shards {
        return Err(resume_error(format!(
            "live epoch length {live_epoch_len} is not below epoch size {epoch_data_shards}"
        )));
    }

    Ok(())
}

fn append_position_after_prefix(map: &FilemarkMap) -> Result<PhysicalPositionHint, ParityError> {
    map.append_position_after_prefix().map_err(|err| match err {
        ParityError::FilemarkMapReconstruct(message) => resume_error(message),
        other => other,
    })
}

fn epoch_data_shards(scheme: &ParityScheme) -> Result<u64, ParityError> {
    u64::from(scheme.data_blocks_per_stripe)
        .checked_mul(u64::from(scheme.stripes_per_neighborhood))
        .filter(|value| *value > 0)
        .ok_or_else(|| resume_error("epoch data-shard count overflows or is zero"))
}

struct ResumeEpochAccumulator {
    scheme: ParityScheme,
    codec: ReedSolomonCodec,
    tape_uuid: [u8; 16],
    block_size: u32,
    epoch_data_shards: u64,
    expected_next_ordinal: u64,
    current_epoch_id: u64,
    current_epoch_start: u64,
    current_epoch_data: u64,
    stripe_buffers: Vec<Vec<Vec<u8>>>,
    data_shard_crc64s: Vec<u64>,
    rebuilt_sidecars: Vec<ResumeRebuiltSidecar>,
}

impl ResumeEpochAccumulator {
    fn new(
        scheme: &ParityScheme,
        tape_uuid: [u8; 16],
        block_size: u32,
        first_ordinal: u64,
        first_epoch_id: u64,
    ) -> Result<Self, ParityError> {
        let epoch_data_shards = epoch_data_shards(scheme)?;
        let codec = ReedSolomonCodec::new(scheme)?;
        let stripe_buffers = (0..scheme.stripes_per_neighborhood)
            .map(|_| Vec::with_capacity(codec.data_blocks()))
            .collect();
        Ok(Self {
            scheme: scheme.clone(),
            codec,
            tape_uuid,
            block_size,
            epoch_data_shards,
            expected_next_ordinal: first_ordinal,
            current_epoch_id: first_epoch_id,
            current_epoch_start: first_ordinal,
            current_epoch_data: 0,
            stripe_buffers,
            data_shard_crc64s: Vec::new(),
            rebuilt_sidecars: Vec::new(),
        })
    }

    fn push_block(&mut self, ordinal: u64, block: Vec<u8>) -> Result<(), ParityError> {
        if ordinal != self.expected_next_ordinal {
            return Err(resume_error(format!(
                "resume rebuild expected ordinal {}, got {ordinal}",
                self.expected_next_ordinal
            )));
        }
        if block.len() != self.block_size as usize {
            return Err(resume_error(format!(
                "resume rebuild ordinal {ordinal} length {} != block_size {}",
                block.len(),
                self.block_size
            )));
        }

        let stripe_index =
            (self.current_epoch_data % u64::from(self.scheme.stripes_per_neighborhood)) as usize;
        let row = self.current_epoch_data / u64::from(self.scheme.stripes_per_neighborhood);
        if row >= u64::from(self.scheme.data_blocks_per_stripe) {
            return Err(resume_error(
                "resume rebuild epoch row exceeds data_blocks_per_stripe",
            ));
        }
        self.data_shard_crc64s.push(data_shard_crc64(&block));
        self.stripe_buffers[stripe_index].push(block);
        self.current_epoch_data += 1;
        self.expected_next_ordinal += 1;

        if self.current_epoch_data == self.epoch_data_shards {
            self.close_full_epoch()?;
        }

        Ok(())
    }

    fn close_full_epoch(&mut self) -> Result<(), ParityError> {
        let s = self.scheme.stripes_per_neighborhood as usize;
        let k = self.codec.data_blocks();
        let m = self.codec.parity_blocks();
        let mut parity_shards = Vec::with_capacity(
            s.checked_mul(m)
                .ok_or_else(|| resume_error("resume rebuild parity shard count overflows"))?,
        );
        for (stripe_index, data) in self.stripe_buffers.iter().enumerate() {
            if data.len() != k {
                return Err(resume_error(format!(
                    "resume rebuild full epoch stripe {stripe_index} has {} shards, expected {k}",
                    data.len()
                )));
            }
            parity_shards.extend(self.codec.encode(data)?);
        }

        let plan = ResumeSidecarPlan {
            epoch_id: self.current_epoch_id,
            protected_ordinal_start: self.current_epoch_start,
            protected_ordinal_end_exclusive: self
                .current_epoch_start
                .checked_add(self.epoch_data_shards)
                .ok_or_else(|| resume_error("resume rebuild sidecar range overflows"))?,
        };
        let descriptor = SidecarDescriptor {
            tape_uuid: self.tape_uuid,
            epoch_id: plan.epoch_id,
            k: self.scheme.data_blocks_per_stripe,
            m: self.scheme.parity_blocks_per_stripe,
            stripes_per_epoch: self.scheme.stripes_per_neighborhood,
            block_size: self.block_size,
            protected_ordinal_start: plan.protected_ordinal_start,
            protected_ordinal_end_exclusive: plan.protected_ordinal_end_exclusive,
        };
        let encoded = encode_sidecar_tape_file(
            &descriptor,
            &parity_shards,
            std::mem::take(&mut self.data_shard_crc64s),
        )?;
        self.rebuilt_sidecars
            .push(ResumeRebuiltSidecar { plan, encoded });

        for stripe in &mut self.stripe_buffers {
            stripe.clear();
        }
        self.current_epoch_start = self
            .current_epoch_start
            .checked_add(self.epoch_data_shards)
            .ok_or_else(|| resume_error("resume rebuild epoch start overflows"))?;
        self.current_epoch_id = self
            .current_epoch_id
            .checked_add(1)
            .ok_or_else(|| resume_error("resume rebuild epoch id overflows"))?;
        self.current_epoch_data = 0;
        Ok(())
    }

    fn finish(
        self,
    ) -> Result<(Vec<ResumeRebuiltSidecar>, Option<ResumeLiveEpochState>), ParityError> {
        let live_epoch = if self.current_epoch_data == 0 {
            None
        } else {
            Some(ResumeLiveEpochState {
                epoch_id: self.current_epoch_id,
                protected_ordinal_start: self.current_epoch_start,
                next_data_ordinal: self.expected_next_ordinal,
                data_blocks_in_epoch: self.current_epoch_data,
                stripe_buffers: self.stripe_buffers,
                data_shard_crc64s: self.data_shard_crc64s,
            })
        };
        Ok((self.rebuilt_sidecars, live_epoch))
    }
}

fn read_committed_object_block(
    source: &mut dyn RawTapeSource,
    map: &FilemarkMap,
    ordinal: u64,
    block_size: u32,
) -> Result<Vec<u8>, ParityError> {
    let tape_position = map.position_for_ordinal(ordinal).map_err(|err| {
        resume_error(format!(
            "resume rebuild could not map ordinal {ordinal} to an object block: {err}"
        ))
    })?;
    let physical = map.physical_position(tape_position).map_err(|err| {
        resume_error(format!(
            "resume rebuild could not map ordinal {ordinal} to a physical position: {err}"
        ))
    })?;
    source.locate_physical(physical).map_err(|err| {
        resume_error(format!(
            "resume rebuild could not locate ordinal {ordinal} at physical LBA {}: {err}",
            physical.lba
        ))
    })?;

    let mut buf = vec![0u8; block_size as usize];
    match source.read_record(&mut buf).map_err(|err| {
        resume_error(format!(
            "resume rebuild failed reading ordinal {ordinal} at physical LBA {}: {err}",
            physical.lba
        ))
    })? {
        RawReadOutcome::Block { bytes, .. } if bytes == block_size as usize => Ok(buf),
        RawReadOutcome::Block { bytes, .. } => Err(resume_error(format!(
            "resume rebuild ordinal {ordinal} read {bytes} bytes, expected {block_size}"
        ))),
        RawReadOutcome::Filemark { .. } => Err(resume_error(format!(
            "resume rebuild ordinal {ordinal} encountered a filemark instead of object data"
        ))),
        RawReadOutcome::EndOfData { .. } => Err(resume_error(format!(
            "resume rebuild ordinal {ordinal} encountered end-of-data instead of object data"
        ))),
    }
}

fn locate_resume_append_position(
    source: &mut dyn RawTapeSource,
    append_position: PhysicalPositionHint,
) -> Result<(), ParityError> {
    source.locate_physical(append_position).map_err(|err| {
        resume_error(format!(
            "resume rebuild could not return to append position {}: {err}",
            append_position.lba
        ))
    })?;
    let actual = source.position().map_err(|err| {
        resume_error(format!(
            "resume rebuild could not verify append position {} after locate: {err}",
            append_position.lba
        ))
    })?;
    if actual != append_position {
        return Err(resume_error(format!(
            "resume rebuild could not return to append position {}: actual position was LBA {} partition {}, expected partition {}",
            append_position.lba, actual.lba, actual.partition, append_position.partition
        )));
    }
    Ok(())
}

fn validate_rebuilt_sidecars(
    rebuilt_sidecars: &[ResumeRebuiltSidecar],
    expected_plans: &[ResumeSidecarPlan],
) -> Result<(), ParityError> {
    if rebuilt_sidecars.len() != expected_plans.len() {
        return Err(resume_error(format!(
            "resume rebuild produced {} sidecars, expected {}",
            rebuilt_sidecars.len(),
            expected_plans.len()
        )));
    }
    for (index, (rebuilt, expected)) in rebuilt_sidecars.iter().zip(expected_plans).enumerate() {
        if &rebuilt.plan != expected {
            return Err(resume_error(format!(
                "resume rebuild sidecar {index} does not match planned epoch/range"
            )));
        }
    }
    Ok(())
}

fn write_rebuilt_sidecar_to_raw(
    sink: &mut dyn RawTapeSink,
    tape_file_number: u32,
    rebuilt: &ResumeRebuiltSidecar,
    expected_tape_uuid: &[u8; 16],
) -> Result<SidecarTapeFile, ParityError> {
    let encoded = &rebuilt.encoded;
    validate_encoded_sidecar_before_write(tape_file_number, rebuilt, expected_tape_uuid)?;
    let block_size = usize::try_from(encoded.header.block_size)
        .map_err(|_| resume_error("resume sidecar block_size overflows usize"))?;
    let expected_block_count = usize::try_from(encoded.header.sidecar_total_block_count)
        .map_err(|_| resume_error("resume sidecar block count overflows usize"))?;
    if encoded.blocks.len() != expected_block_count {
        return Err(resume_error(format!(
            "resume sidecar {} has {} encoded blocks, expected {expected_block_count}",
            tape_file_number,
            encoded.blocks.len()
        )));
    }

    for (block_index, block) in encoded.blocks.iter().enumerate() {
        if block.len() != block_size {
            return Err(resume_error(format!(
                "resume sidecar {tape_file_number} block {block_index} length {} != block_size {block_size}",
                block.len()
            )));
        }
        match sink.write_fixed_block(block).map_err(|err| {
            resume_error(format!(
                "resume sidecar {tape_file_number} block {block_index} write failed before filemark: {err}"
            ))
        })? {
            RawWriteOutcome::WroteBlock { end_of_medium, .. } => {
                if end_of_medium {
                    return Err(resume_error(format!(
                        "resume sidecar {tape_file_number} block {block_index} reached end-of-medium before filemark; catalog commit is forbidden"
                    )));
                }
            }
            RawWriteOutcome::WroteFilemark { .. } => {
                return Err(resume_error(format!(
                    "resume sidecar {tape_file_number} write_fixed_block returned a filemark outcome"
                )));
            }
        }
    }

    let barrier = sink.write_filemarks(1, false).map_err(|err| {
        resume_error(format!(
            "resume sidecar {tape_file_number} synchronous filemark failed before catalog commit: {err}"
        ))
    })?;
    let (barrier_position, early_warning, end_of_medium) = match barrier {
        RawWriteOutcome::WroteFilemark {
            position_after,
            early_warning,
            end_of_medium,
        } => (position_after, early_warning, end_of_medium),
        RawWriteOutcome::WroteBlock { .. } => {
            return Err(resume_error(format!(
                "resume sidecar {tape_file_number} write_filemark returned a block outcome"
            )));
        }
    };
    if end_of_medium {
        return Err(resume_error(format!(
            "resume sidecar {tape_file_number} synchronous filemark reached end-of-medium before catalog commit"
        )));
    }

    let captured_position = sink.position().map_err(|err| {
        resume_error(format!(
            "resume sidecar {tape_file_number} could not capture post-filemark position: {err}"
        ))
    })?;
    if captured_position != barrier_position {
        return Err(resume_error(format!(
            "resume sidecar {tape_file_number} post-filemark position capture LBA {} != barrier LBA {}",
            captured_position.lba, barrier_position.lba
        )));
    }

    Ok(SidecarTapeFile {
        tape_file_number,
        epoch_id: rebuilt.plan.epoch_id,
        block_count: encoded.blocks.len() as u64,
        protected_ordinal_start: rebuilt.plan.protected_ordinal_start,
        protected_ordinal_end_exclusive: rebuilt.plan.protected_ordinal_end_exclusive,
        sidecar_header_block_count: encoded.header.shard_index_block_count,
        parity_shard_block_count: encoded.header.parity_block_count,
        canonical_metadata_hash: encoded.header.canonical_metadata_hash,
        final_partial_epoch: encoded.header.real_data_shard_count
            < encoded.header.logical_shard_count,
        filemark_outcome: WriteFilemarksOutcome::from_device_position(
            early_warning,
            end_of_medium,
            physical_to_tape_position(captured_position),
        ),
    })
}

fn validate_encoded_sidecar_before_write(
    tape_file_number: u32,
    rebuilt: &ResumeRebuiltSidecar,
    expected_tape_uuid: &[u8; 16],
) -> Result<(), ParityError> {
    let encoded = &rebuilt.encoded;
    // Deliberate round-trip safety check: the bytes about to be written must
    // be exactly what a later catalog-less scan/restart will decode.
    let decoded = parse_sidecar_tape_file(&encoded.blocks, expected_tape_uuid)?;
    if decoded.header != encoded.header || decoded.index != encoded.index {
        return Err(resume_error(format!(
            "resume sidecar {tape_file_number} encoded sidecar metadata does not match its block bytes"
        )));
    }
    let h = usize::try_from(encoded.header.shard_index_block_count)
        .map_err(|_| resume_error("resume sidecar index block count overflows usize"))?;
    let p = usize::try_from(encoded.header.parity_block_count)
        .map_err(|_| resume_error("resume sidecar parity block count overflows usize"))?;
    let parity_end = h
        .checked_add(p)
        .ok_or_else(|| resume_error("resume sidecar parity block range overflows"))?;
    if decoded.parity_shards.as_slice() != &encoded.blocks[h..parity_end] {
        return Err(resume_error(format!(
            "resume sidecar {tape_file_number} decoded parity shards do not match encoded block bytes"
        )));
    }
    if encoded.header.epoch_id != rebuilt.plan.epoch_id
        || encoded.header.protected_ordinal_start != rebuilt.plan.protected_ordinal_start
        || encoded.header.protected_ordinal_end_exclusive
            != rebuilt.plan.protected_ordinal_end_exclusive
    {
        return Err(resume_error(format!(
            "resume sidecar {tape_file_number} encoded header does not match planned epoch/range"
        )));
    }
    Ok(())
}

fn physical_to_tape_position(position: PhysicalPositionHint) -> TapePosition {
    TapePosition {
        lba: position.lba,
        partition: position.partition,
        beginning_of_partition: position.lba == 0,
        end_of_partition: false,
        block_position_end_of_warning: false,
    }
}

