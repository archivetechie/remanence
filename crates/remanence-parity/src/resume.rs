fn emit_resume_rebuilt_sidecars_to_raw_inner<F>(
    sink: &mut dyn RawTapeSink,
    mut journal: Option<&mut dyn TapeFileJournal>,
    plan: ResumeAppendPlan,
    rebuilt_sidecars: &[ResumeRebuiltSidecar],
    expected_tape_uuid: [u8; 16],
    mut commit_sidecar: F,
) -> Result<ResumeAppendResult, ParityError>
where
    F: FnMut(&SidecarTapeFile) -> Result<(), ParityError>,
{
    if journal
        .as_ref()
        .is_some_and(|journal| journal.tape_uuid() != expected_tape_uuid)
    {
        return Err(resume_error(
            "journal tape UUID does not match resume sidecar tape UUID",
        ));
    }
    validate_rebuilt_sidecars(rebuilt_sidecars, &plan.sidecars_to_emit)?;

    let start_position = sink.position().map_err(|err| {
        resume_error(format!(
            "resume sidecar emission could not capture append position: {err}"
        ))
    })?;
    if start_position != plan.append_position {
        return Err(resume_error(format!(
            "resume sidecar raw sink starts at LBA {}, expected append position {}",
            start_position.lba, plan.append_position.lba
        )));
    }

    let mut emitted = Vec::with_capacity(rebuilt_sidecars.len());
    let mut expected_next_position = start_position;
    let mut durable_boundary = DurableBoundaryState::from_last_committed_tape_file_number(Some(
        plan.append_after_tape_file_number,
    ));
    for (index, rebuilt) in rebuilt_sidecars.iter().enumerate() {
        let expected_tape_file_number = plan
            .append_after_tape_file_number
            .checked_add(1)
            .and_then(|value| value.checked_add(u32::try_from(index).ok()?))
            .ok_or_else(|| resume_error("resume sidecar tape-file number overflows"))?;
        if index > 0 {
            let current_position = sink.position().map_err(|err| {
                resume_error(format!(
                    "resume sidecar {expected_tape_file_number} could not verify inter-sidecar position: {err}"
                ))
            })?;
            if current_position != expected_next_position {
                return Err(resume_error(format!(
                    "resume sidecar {expected_tape_file_number} raw sink starts at LBA {}, expected previous sidecar barrier position {}",
                    current_position.lba, expected_next_position.lba
                )));
            }
        }
        durable_boundary
            .begin_tape_file(TapeFileKind::ParitySidecar, expected_tape_file_number)
            .map_err(resume_boundary_error)?;
        let summary = match write_rebuilt_sidecar_to_raw(
            sink,
            expected_tape_file_number,
            rebuilt,
            &expected_tape_uuid,
        ) {
            Ok(summary) => summary,
            Err(err) => {
                return Err(abandon_resume_sidecar_boundary_or(
                    &mut durable_boundary,
                    expected_tape_file_number,
                    err,
                ));
            }
        };
        expected_next_position = summary.filemark_outcome.position_after.into();
        if let Err(err) = commit_sidecar(&summary) {
            return Err(abandon_resume_sidecar_boundary_or(
                &mut durable_boundary,
                expected_tape_file_number,
                resume_error(format!(
                    "resume sidecar {} catalog commit failed after filemark barrier: {err}",
                    summary.tape_file_number
                )),
            ));
        }
        durable_boundary
            .commit_tape_file(TapeFileKind::ParitySidecar, expected_tape_file_number)
            .map_err(resume_boundary_error)?;
        if let Some(journal) = journal.as_deref_mut() {
            journal.commit_bundle(&crate::journal::CommittedBundle {
                kind: crate::journal::CommittedBundleKind::ResumeSidecars,
                entries: vec![summary.tape_file_entry()],
                highest_protected_ordinal: summary.protected_ordinal_end_exclusive,
                total_committed_ordinals: plan.next_data_ordinal,
            })?;
        }
        emitted.push(summary);
    }

    plan.complete(emitted)
}

fn abandon_resume_sidecar_boundary_or(
    durable_boundary: &mut DurableBoundaryState,
    tape_file_number: u32,
    err: ParityError,
) -> ParityError {
    match durable_boundary.abandon_tape_file(TapeFileKind::ParitySidecar, tape_file_number) {
        Ok(_) => err,
        Err(boundary_err) => resume_boundary_error(boundary_err),
    }
}

fn resume_boundary_error(err: ParityError) -> ParityError {
    match err {
        ParityError::Invariant(message) => resume_error(format!(
            "resume durable-boundary state rejected transition: {message}"
        )),
        other => other,
    }
}

/// Build the restart/append plan from the catalog-committed filemark-map
/// prefix.
///
/// The caller is Layer 5: it loads
/// `catalog_tape_files WHERE tape_id = ? AND committed = true ORDER BY tape_file_number`,
/// verifies that catalog prefix, and passes the resulting [`FilemarkMap`] here.
pub fn plan_resume_append_from_committed_prefix(
    committed_prefix: &FilemarkMap,
    scheme: &ParityScheme,
) -> Result<ResumeAppendPlan, ParityError> {
    plan_resume_append_from_committed_prefix_with_mode(
        committed_prefix,
        scheme,
        ResumePlanningMode::V1Committed,
    )
}

#[derive(Clone, Copy, Debug, PartialEq, Eq)]
enum ResumePlanningMode {
    V1Committed,
    LegacyForensic,
}

fn plan_resume_append_from_committed_prefix_with_mode(
    committed_prefix: &FilemarkMap,
    scheme: &ParityScheme,
    mode: ResumePlanningMode,
) -> Result<ResumeAppendPlan, ParityError> {
    scheme.validate()?;
    let entries = committed_prefix.entries();
    let last = entries
        .last()
        .ok_or_else(|| resume_error("committed prefix has no tape files"))?;
    let append_position = append_position_after_prefix(committed_prefix)?;
    let epoch_data_shards = epoch_data_shards(scheme)?;
    let total_ordinals = committed_prefix.total_data_ordinals();
    let (highest_before, next_epoch_id) =
        validate_and_measure_epoch_ranges(committed_prefix, total_ordinals, epoch_data_shards)?;

    if highest_before > total_ordinals {
        return Err(resume_error(format!(
            "protection watermark {highest_before} exceeds committed data ordinals {total_ordinals}"
        )));
    }
    let rebuild_data_shards = total_ordinals - highest_before;
    if mode == ResumePlanningMode::V1Committed && rebuild_data_shards >= epoch_data_shards {
        return Err(resume_error(format!(
            "committed v1 prefix has {rebuild_data_shards} unprotected ordinals, \
             reaching the restart bound of one full epoch ({epoch_data_shards}); \
             multi-epoch rebuild is legacy/forensic only"
        )));
    }
    let full_rebuild_epochs = rebuild_data_shards / epoch_data_shards;
    let mut sidecars_to_emit = Vec::new();
    for offset in 0..full_rebuild_epochs {
        let start = highest_before
            .checked_add(offset.checked_mul(epoch_data_shards).ok_or_else(|| {
                resume_error("resume sidecar ordinal range multiplication overflows")
            })?)
            .ok_or_else(|| resume_error("resume sidecar ordinal range overflows"))?;
        let end = start
            .checked_add(epoch_data_shards)
            .ok_or_else(|| resume_error("resume sidecar ordinal range overflows"))?;
        sidecars_to_emit.push(ResumeSidecarPlan {
            epoch_id: next_epoch_id
                .checked_add(offset)
                .ok_or_else(|| resume_error("resume sidecar epoch id overflows"))?,
            protected_ordinal_start: start,
            protected_ordinal_end_exclusive: end,
        });
    }

    let highest_after = highest_before
        .checked_add(
            full_rebuild_epochs
                .checked_mul(epoch_data_shards)
                .ok_or_else(|| resume_error("resume watermark multiplication overflows"))?,
        )
        .ok_or_else(|| resume_error("resume watermark overflows"))?;

    validate_tail_coherence(last, total_ordinals, highest_after, epoch_data_shards)?;

    Ok(ResumeAppendPlan {
        append_after_tape_file_number: last.tape_file_number,
        append_position,
        highest_protected_ordinal_before_rebuild: highest_before,
        highest_protected_ordinal_after_rebuild: highest_after,
        live_epoch_start: highest_after,
        next_data_ordinal: total_ordinals,
        next_epoch_id: next_epoch_id
            .checked_add(full_rebuild_epochs)
            .ok_or_else(|| resume_error("resume live epoch id overflows"))?,
        sidecars_to_emit,
    })
}

/// Validate the epoch labels and descriptor ranges carried by committed
/// sidecars, returning `(protected_end, next_epoch_id)` for the open range.
fn validate_and_measure_epoch_ranges(
    committed_prefix: &FilemarkMap,
    total_ordinals: u64,
    epoch_data_shards: u64,
) -> Result<(u64, u64), ParityError> {
    let mut expected_start = 0u64;
    let mut expected_epoch_id = 0u64;
    for entry in committed_prefix
        .entries()
        .iter()
        .filter(|entry| entry.kind == TapeFileKind::ParitySidecar)
    {
        let epoch_id = entry
            .epoch_id
            .ok_or_else(|| resume_error("sidecar is missing epoch id"))?;
        let start = entry
            .protected_ordinal_start
            .ok_or_else(|| resume_error("sidecar is missing protected range start"))?;
        let end = entry
            .protected_ordinal_end_exclusive
            .ok_or_else(|| resume_error("sidecar is missing protected range end"))?;
        if epoch_id != expected_epoch_id {
            return Err(resume_error(format!(
                "sidecar epoch id {epoch_id} is not the expected monotonic id {expected_epoch_id}"
            )));
        }
        if start != expected_start {
            return Err(resume_error(format!(
                "sidecar epoch {epoch_id} starts at ordinal {start}, expected contiguous start {expected_start}"
            )));
        }
        if end > total_ordinals {
            return Err(resume_error(format!(
                "sidecar epoch {epoch_id} ends at ordinal {end}, beyond committed data ordinals {total_ordinals}"
            )));
        }
        let range_len = end.checked_sub(start).ok_or_else(|| {
            resume_error(format!(
                "sidecar epoch {epoch_id} has descending range [{start}, {end})"
            ))
        })?;
        if range_len == 0 || range_len > epoch_data_shards {
            return Err(resume_error(format!(
                "sidecar epoch {epoch_id} range length {range_len} is outside 1..={epoch_data_shards}"
            )));
        }
        expected_start = end;
        expected_epoch_id = expected_epoch_id
            .checked_add(1)
            .ok_or_else(|| resume_error("sidecar epoch id overflows"))?;
    }
    Ok((expected_start, expected_epoch_id))
}

